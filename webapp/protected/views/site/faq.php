  </div>
	  <div class="panel-body">
	  	Absolutly, remember that Stirplate is a product made by a scientists who knows how much hard work goes into creating a single dataset. 
	  	Here is a small list of how your data is safe with us:
	  	<br>
	  	<br>
		<li> Revision history: Sometimes we make mistakes with our files or delete them by accident. No problem, you can simply go back (up to 25 different versions) and fix that file. Simple. </li>
		<li>All files are backed up on multiple different servers. Even if you delete a file, we can recover it quickly. </li>
		<li> All files stored on Stirplate are encrypted, sent security via SSL and are even guarded by armed guards. </li>
  	  </div>
