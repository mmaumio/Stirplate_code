    <h3 class="panel-title">What happens to my data if stirplate.io closes?</h3>
  </div>
  <div class="panel-body">
	We aren’t going anywhere, but if something was to happen, we can send you all of your files/info in the same organization that you currently have them on our system (by project). You will never lose your data on Stirplate. 
  </div>
</div>

<div class="panel panel-default">
	  <div class="panel-heading">
	    <h3 class="panel-title">Is my scientific data shared with the public?</h3>
	  </div>
  <div class="panel-body">
	No, everything is private unless you choose to share it with others. If you have a dataset that you would like to be made public to fit into the requirements set by NIH and NSF however, we will happily host those datasets for free(open only to other scientists). The Stirplate team loves open science, but we understand that data needs to be kept private while it’s being worked on. No one wants to get scooped and we take your data security seriously. 
  </div>
</div>

<div class="panel panel-default">
