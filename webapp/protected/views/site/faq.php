	    <h3 class="panel-title">I use dropbox or my own servers, why is this better?</h3>
	  </div>
  <div class="panel-body">We love Dropbox and think they are doing a great job. In fact, we use these services at Stirplate. Stirplate is different in the following ways:<br><br>
<li> Files stored on Stirplate are stored on HIPPA compliant servers at no cost to you. 
<li> Stirplate is made for scientists. File sharing is a big part of this, but being able to communicate in 1 centralized location, AND send files, and view/edit the files in one place is something that the other groups can’t offer </li>
<li> We automate your analysis and centralize it. No excel, no graphing programs, and no need to make sure everyone has the same version of the software. One place to do your analysis, send the results to collaborators, and communicate about those data.</li>
<li> We’ve made it easier to view your files without having to drill down through a file structure. You don’t have to tell your collaborators that the file is located in /Project2/Immuno/Testresults/12-13-13/file2.xls. Simply star it on the project and ou are done. </li>  
<li> Store for free: Any public dataset that is on Stirplate will not count towards your data quota. So that preliminary data, or negative data sitting around collecting dust could be used to answer more questions and getting you credit for your work. 
