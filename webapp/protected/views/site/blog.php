    <h3 class="panel-title">Why a USB harddrive is not the way to store your research data.</h3>
  </div>
  <div class="panel-body">
    In every lab I’ve worked in, storing data on USB keys or USB hard drives was the preferred method of sharing files. I don’t know if I bring bad karma with me, but in every lab one of the following happened.
<br>
<li> No one knew which hard drive had the data they wanted (This was most frequent when a student left/graduated or rotated into a new lab)
<li> My PI would ask me to go find some files and then hand me 4 USB hard drives because they had no idea which one had the needed files.
<li>  I’m the recipient of the most dreaded question, "can you fix this?" I typically ended up being the IT guy for a lab, so I was always happy to help, but when it came to broken hard drives, I always cringed at that question. Most of the time the answer is no, if a drive has really died, I wasn't able to fix it. That means everything on that drive was gone, unless it was sent out to be rebuilt, and no one had the money to do that. I actually saw someone lose 6 months of work.No backups and the drive was completley frozen. 
<br>
<br>
If you have to store stuff on a USB drive, make sure you back it up. This is one of the reasons why I started Stirplate, backing files up was always a difficult lesson for people, so I tried to make it an easier process.
